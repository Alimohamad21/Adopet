class Pet {
    constructor(type, image, name, description, age, color, breed, gender, isNeutered, vaccinations) {
        this.type = type;
        this.image = image;
        this.name = name;
        this.description = description;
        this.age = age;
        this.color = color;
        this.breed = breed;
        this.gender = gender;
        this.isNeutered = isNeutered;
        this.vaccinations = vaccinations;
    }
}

export default Pet;
